def dobro(numero):
    """
    Retorna o dobro de um número.
    
    Parâmetros:
        numero (int/float): O número a ser dobrado.
    
    Retorna:
        int/float: O dobro do número.
    """
    return numero * 2


def metade(numero):
    """
    Retorna a metade de um número.
    
    Parâmetros:
        numero (int/float): O número a ser dividido.
    
    Retorna:
        int/float: A metade do número.
    """
    return numero / 2


def quadrado(numero):
    """
    Retorna o quadrado de um número.
    
    Parâmetros:
        numero (int/float): O número a ser elevado.
    
    Retorna:
        int/float: O quadrado do número.
    """
    return numero ** 2

